--총판매금액
SELECT SUM(AMOUNT) FROM SALES;

--제품별 판매금액
SELECT PROD_NAME, SUM(QUANTITY) 수량합, SUM(AMOUNT) 판매금액합
FROM SALES S, PRODUCTS P
WHERE P.PROD_NO = S.PROD_NO
GROUP BY PROD_NAME;

--고객별 수량과 판매금액 합
SELECT CU_NAME, SUM(QUANTITY) 수량합, SUM(AMOUNT) 판매금액합
FROM SALES S, CUSTOMER C
WHERE C.CUST_ID = S.CUST_ID
GROUP BY CU_NAME;

--고객별 제품별 수량과 판매금액합
SELECT CU_NAME, PROD_NAME,SUM(QUANTITY) 수량합, SUM(AMOUNT) 판매금액합
FROM SALES S, CUSTOMER C, PRODUCTS P
WHERE C.CUST_ID = S.CUST_ID AND S.PROD_NO=P.PROD_NO
GROUP BY CU_NAME, PROD_NAME;

