ROLLBACK;

--SAL을 1000미만은 10%, 1000~1500은 5%, 1500초과는 3% 인상
UPDATE EMP
SET SAL=SAL*1.1
WHERE SAL<1000;

UPDATE EMP
SET SAL=SAL*1.05
WHERE SAL BETWEEN 1000 AND 1500;

UPDATE EMP
SET SAL=SAL*1.03
WHERE SAL>1500;

UPDATE EMP
SET SAL = CASE WHEN SAL>1500 THEN SAL*1.03
                            WHEN SAL BETWEEN 1000 AND 1500 THEN SAL*1.05
                            ELSE SAL *1.1
                    END;


